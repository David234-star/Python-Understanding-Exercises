# Arithmetic operators' operations
print("2+2: ", 2+2)  # addition operation
print("12-4: ", 12-4)  # subtraction operation
print("12*2: ", 12*2)  # multiplication operation
print("12/3: ", 12/3)  # division operation
print("12//2: ", 12//2)  # floor division operation
print("12%4: ", 12 % 4)  # modulus operation
print("12**2: ", 12**2)  # exponentiation operation
# Expression level operations
print("(2+2)*3+(12-11)**2/(12*4): ", (2+2)*3 +
      (12-11)**2/(12*4))  # expression operations
